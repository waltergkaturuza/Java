public class ForLPMultiplcation {
    public static void main(String[] args) {
        //multiplication table
        for(int i=1;i<=100; i++){
            System.out.println("-------------------");
            for (int a =1; a <= 10; a++){
                System.out.println(i + " x " + a +" =" + (i*a));
               // System.out.println("-------------------");
            }
        }
    }
}
